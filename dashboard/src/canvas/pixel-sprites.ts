// ── Palette Resolution ───────────────────────────────────────────────────

export function resolvePalette(role: CrewRole): string[] {
  const palette = CREW_PALETTES[role];

  return [
    "transparent", // 0
    `hsl(0, 0%, ${palette.skinL}%)`, // 1 = skin
    `hsl(${palette.hue}, ${palette.sat}%, ${palette.bodyL}%)`, // 2 = body
    `hsl(${palette.hue}, ${palette.sat}%, ${palette.trimL}%)`, // 3 = trim
    palette.accent, // 4 = accent
    "hsl(20, 60%, 35%)", // 5 = hair (brown)
    "#000000", // 6 = eye (black)
    "hsl(0, 0%, 15%)", // 7 = boot/shoe (dark)
  ];
}

// ── Sprite Rendering ────────────────────────────────────────────────────

const spriteCache = new Map<string, HTMLCanvasElement>();

export function renderSprite(
  role: CrewRole,
  action: SpriteAction,
  frame: number,
  direction: Direction,
  scale = 2,
): HTMLCanvasElement {
  const cacheKey = `${role}:${action}:${frame}:${direction}:${scale}`;

  if (spriteCache.has(cacheKey)) {
    return spriteCache.get(cacheKey)!;
  }

  const palette = resolvePalette(role);
  const frames = FRAME_SETS[action];
  const pixelGrid = frames[frame % frames.length];

  const renderedWidth = PIXEL_WIDTH * scale;
  const renderedHeight = PIXEL_HEIGHT * scale;

  const canvas = document.createElement("canvas");
  canvas.width = renderedWidth;
  canvas.height = renderedHeight;

  const ctx = canvas.getContext("2d")!;
  ctx.imageSmoothingEnabled = false;

  // Render each pixel
  for (let y = 0; y < PIXEL_HEIGHT; y++) {
    for (let x = 0; x < PIXEL_WIDTH; x++) {
      let pixelX = x;

      // Mirror horizontally if facing right
      if (direction === "right") {
        pixelX = PIXEL_WIDTH - 1 - x;
      }

      const paletteIndex = pixelGrid[y][pixelX];

      if (paletteIndex !== 0) {
        ctx.fillStyle = palette[paletteIndex];
        ctx.fillRect(x * scale, y * scale, scale, scale);
      }
    }
  }

  spriteCache.set(cacheKey, canvas);
  return canvas;
}

// ── Spawn/Despawn Effects ───────────────────────────────────────────────

export function getSpawnMask(progress: number): boolean[] {
  const mask: boolean[] = new Array(PIXEL_WIDTH * PIXEL_HEIGHT).fill(false);
  const clampedProgress = Math.max(0, Math.min(1, progress));
  const revealRow = Math.floor(PIXEL_HEIGHT * clampedProgress);

  for (let y = 0; y < revealRow; y++) {
    for (let x = 0; x < PIXEL_WIDTH; x++) {
      mask[y * PIXEL_WIDTH + x] = true;
    }
  }

  return mask;
}

export function getDespawnOffsets(
  progress: number,
): { dx: number; dy: number }[] {
  const offsets: { dx: number; dy: number }[] = [];
  const clampedProgress = Math.max(0, Math.min(1, progress));

  for (let i = 0; i < PIXEL_WIDTH * PIXEL_HEIGHT; i++) {
    const scatter = clampedProgress * 8;
    const angle = (i / (PIXEL_WIDTH * PIXEL_HEIGHT)) * Math.PI * 2;
    const distance = scatter;

    offsets.push({
      dx: Math.cos(angle) * distance,
      dy: Math.sin(angle) * distance,
    });
  }

  return offsets;
}

